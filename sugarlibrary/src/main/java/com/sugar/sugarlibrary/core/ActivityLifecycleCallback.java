package com.sugar.sugarlibrary.core;

import android.app.Activity;
import android.app.Application;
import android.os.Bundle;

import timber.log.Timber;



/**
 * @author wobiancao
 * @date 2019/5/15
 * desc :activity生命周期管理
 */
public class ActivityLifecycleCallback implements Application.ActivityLifecycleCallbacks{

    public static ActivityLifecycleCallback getInstance() {
        return HolderClass.INSTANCE;
    }

    private final static class HolderClass {
        private final static ActivityLifecycleCallback INSTANCE = new ActivityLifecycleCallback();
    }


    private ActivityLifecycleCallback() {}

    /**
     * 必须在 Application 的 onCreate 方法中调用
     */
    public void init(Application application) {
        application.registerActivityLifecycleCallbacks(this);
    }

    @Override
    public void onActivityCreated(Activity activity, Bundle bundle) {
        AppManager.getAppManager().addActivity(activity);
        Timber.i(activity + " - onActivityCreated");
    }

    @Override
    public void onActivityStarted(Activity activity) {
        Timber.i(activity + " - onActivityStarted");
    }

    @Override
    public void onActivityResumed(Activity activity) {
        Timber.i(activity + " - onActivityResumed");
    }

    @Override
    public void onActivityPaused(Activity activity) {
        Timber.i(activity + " - onActivityPaused");
    }

    @Override
    public void onActivityStopped(Activity activity) {
        Timber.i(activity + " - onActivityStopped");
    }

    @Override
    public void onActivitySaveInstanceState(Activity activity, Bundle bundle) {
        Timber.i(activity + " - onActivitySaveInstanceState");
    }

    @Override
    public void onActivityDestroyed(Activity activity) {
        Timber.i(activity + " - onActivityDestroyed");
        AppManager.getAppManager().removeActivity(activity);
    }
}
